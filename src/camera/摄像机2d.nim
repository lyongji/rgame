## 2D 游戏摄像机
##
## 提取自 raylib 官方示例，支持 5 种跟随模式、缩放至光标、屏幕震动。
##
## 跟随模式:
##   居中跟随     — 简单的中心跟随
##   居中带边界   — 中心跟随 + offset 边界约束（地图边缘自适应）
##   平滑跟随     — 平滑中心跟随 + 最小速度/距离
##   落地平滑     — 水平持续跟随，垂直落地后平滑归位
##   边界推动     — 玩家靠近屏幕边缘时推动摄像机
##
## 用法:
##   import camera/摄像机2d
##   var 摄像机 = 新建摄像机2d()
##   摄像机.开始跟随(玩家位置)
##   while not windowShouldClose():
##     摄像机.更新()
##     beginMode2D(摄像机.转Raylib())
##       ...
##     endMode2D()

import vmath
import raylib
import mymath/vmath_raylib
import std/random
import std/math

export vmath

# ============================================================================
# 类型定义
# ============================================================================

type
  跟随模式* = enum
    居中跟随       ## 简单的中心跟随
    居中带边界     ## 中心跟随 + offset 边界约束
    平滑跟随       ## 平滑中心跟随 + 最小速度/距离
    落地平滑       ## 水平持续跟随，垂直落地后平滑归位
    边界推动       ## 玩家靠近屏幕边缘时推动摄像机

  摄像机2d* = object
    ## 2D 摄像机

    # ── 核心状态 ──────────────────────────────────
    位置*:      Vec2    ## 摄像机聚焦点（世界坐标）
    缩放*:      float32 ## 缩放倍率（1.0 = 原始大小）
    旋转*:      float32 ## 旋转角度（角度制）

    # ── 跟随配置 ──────────────────────────────────
    模式*:         跟随模式
    跟随目标*:     Vec2
    是否正在跟随*: bool
    跟随速度*:     float32   ## 平滑跟随插值系数 (0~1)
    最小速度*:     float32   ## 平滑跟随最小移动速度（像素/秒）
    最小生效距离*: float32   ## 平滑跟随最小触发距离

    # ── 落地平滑状态机 ────────────────────────────
    是否正在归位*: bool
    归位目标Y*:  float32
    归位速度*:   float32

    # ── 边界推动 ──────────────────────────────────
    推动边界比*:  Vec2    ## 屏幕宽高比，作为推动触发边界

    # ── 屏幕震动 ──────────────────────────────────
    震动强度*:  float32
    震动衰减*:  float32
    屏幕偏移*:  Vec2      ## 震动叠加的屏幕空间偏移

    # ── offset 修正（居中带边界模式使用） ────────
    屏幕偏移修正*: Vec2   ## 在屏幕中心基础上叠加的 offset 偏移

    # ── 世界边界 ──────────────────────────────────
    是否启用边界*: bool
    世界左*:  float32
    世界右*:  float32
    世界上*:  float32
    世界下*:  float32

# ============================================================================
# 构造函数
# ============================================================================

func 新建摄像机2d*(
  位置: Vec2 = vec2(0, 0),
  缩放: float32 = 1.0,
  旋转: float32 = 0.0,
  模式: 跟随模式 = 平滑跟随,
  跟随速度: float32 = 0.1,
): 摄像机2d {.inline.} =
  摄像机2d(
    位置:        位置,
    缩放:        缩放,
    旋转:        旋转,
    模式:        模式,
    跟随速度:    跟随速度,
    最小速度:     30,
    最小生效距离: 10,
    震动衰减:     0.9,
    归位速度:     700,
    推动边界比:   vec2(0.2, 0.2),
  )

# ============================================================================
# 与 raylib Camera2D 互转
# ============================================================================

proc 转Raylib*(c: 摄像机2d): Camera2D {.inline.} =
  ## → raylib Camera2D
  let 屏幕宽 = getScreenWidth().float32
  let 屏幕高 = getScreenHeight().float32
  Camera2D(
    offset:   vec2(屏幕宽 / 2, 屏幕高 / 2) + c.屏幕偏移 + c.屏幕偏移修正,
    target:   c.位置,
    rotation: c.旋转,
    zoom:     c.缩放,
  )

# ============================================================================
# 坐标转换
# ============================================================================

proc 屏幕转世界*(c: 摄像机2d; 屏幕坐标: Vec2): Vec2 {.inline.} =
  getScreenToWorld2D(屏幕坐标, c.转Raylib()).toVmath()

proc 世界转屏幕*(c: 摄像机2d; 世界坐标: Vec2): Vec2 {.inline.} =
  getWorldToScreen2D(世界坐标, c.转Raylib()).toVmath()

proc 鼠标位置世界*(c: 摄像机2d): Vec2 {.inline.} =
  c.屏幕转世界(getMousePosition().toVmath())

# ============================================================================
# 屏幕震动
# ============================================================================

proc 触发震动*(c: var 摄像机2d; 强度: float32; 衰减: float32 = 0.9) {.inline.} =
  c.震动强度 = max(强度, 0)
  c.震动衰减 = clamp(衰减, 0.01, 0.99)

# ============================================================================
# 世界边界
# ============================================================================

proc 设置世界边界*(c: var 摄像机2d; 左, 右, 上, 下: float32) {.inline.} =
  c.是否启用边界 = true
  c.世界左 = 左; c.世界右 = 右
  c.世界上 = 上; c.世界下 = 下

proc 关闭边界*(c: var 摄像机2d) {.inline.} =
  c.是否启用边界 = false

# ============================================================================
# 缩放至鼠标光标
# ============================================================================

proc 缩放至光标*(c: var 摄像机2d; 滚轮值: float32;
                  缩放下限: float32 = 0.125;
                  缩放上限: float32 = 64.0) =
  ## 滚轮缩放：保持鼠标光标下的世界点不动
  if 滚轮值 == 0: return

  let 鼠标屏幕 = getMousePosition().toVmath()
  let 鼠标世界 = c.屏幕转世界(鼠标屏幕)
  let 屏幕中心 = vec2(getScreenWidth().float32 / 2, getScreenHeight().float32 / 2)

  # 对数缩放
  let 新缩放 = clamp(c.缩放 * exp(0.2 * 滚轮值), 缩放下限, 缩放上限)

  # 保持鼠标下世界点不动：调整位置使光标映射到同一世界点
  let 光标偏移 = (鼠标屏幕 - 屏幕中心) / 新缩放
  c.位置 = 鼠标世界 - 光标偏移
  c.缩放 = 新缩放

# ============================================================================
# 跟随模式实现
# ============================================================================

proc 更新居中跟随*(c: var 摄像机2d) =
  ## 模式：简单中心跟随
  c.位置 = c.跟随目标
  c.屏幕偏移修正 = vec2(0, 0)

proc 更新居中带边界*(c: var 摄像机2d) =
  ## 模式：中心跟随 + offset 边界约束
  ## 当地图边缘进入屏幕时，推 offset 阻止出现空白（用屏幕偏移修正实现）
  c.位置 = c.跟随目标
  c.屏幕偏移修正 = vec2(0, 0)

  if not c.是否启用边界: return

  let 屏幕宽 = getScreenWidth().float32
  let 屏幕高 = getScreenHeight().float32

  # 计算地图四个角点的屏幕坐标
  let 角点 = [
    c.世界转屏幕(vec2(c.世界左, c.世界上)),
    c.世界转屏幕(vec2(c.世界右, c.世界上)),
    c.世界转屏幕(vec2(c.世界左, c.世界下)),
    c.世界转屏幕(vec2(c.世界右, c.世界下)),
  ]
  var 最小 = 角点[0]
  var 最大 = 角点[0]
  for i in 1..3:
    最小.x = min(最小.x, 角点[i].x)
    最小.y = min(最小.y, 角点[i].y)
    最大.x = max(最大.x, 角点[i].x)
    最大.y = max(最大.y, 角点[i].y)

  # 修正 offset（屏幕空间）使地图边缘紧贴屏幕边缘
  # 正修正 → offset 右/下移 → 视口左/上移 → 地图边缘向右/下移
  var 修正 = vec2(0, 0)
  if 最大.x < 屏幕宽: 修正.x = 屏幕宽 - 最大.x     # 右边空白 → offset 右推
  if 最大.y < 屏幕高: 修正.y = 屏幕高 - 最大.y     # 下边空白 → offset 下推
  if 最小.x > 0:      修正.x = -最小.x              # 左边空白 → offset 左推
  if 最小.y > 0:      修正.y = -最小.y              # 上边空白 → offset 上推

  c.屏幕偏移修正 = 修正

proc 更新平滑跟随*(c: var 摄像机2d; 帧时间: float32) =
  ## 模式：平滑中心跟随 + 最小速度/距离
  c.屏幕偏移修正 = vec2(0, 0)
  let 差值 = c.跟随目标 - c.位置
  let 距离 = length(差值)

  if 距离 > c.最小生效距离:
    let 速度率 = max(c.跟随速度 * 距离, c.最小速度)
    c.位置 = c.位置 + 差值 * min(速度率 * 帧时间 / 距离, 1.0)

proc 更新落地平滑*(c: var 摄像机2d; 帧时间: float32) =
  ## 模式：水平持续跟随，垂直落地后平滑归位
  c.屏幕偏移修正 = vec2(0, 0)
  c.位置.x = c.跟随目标.x

  if c.是否正在归位:
    let 方向 = if c.归位目标Y > c.位置.y: 1.0 else: -1.0
    c.位置.y += 方向 * c.归位速度 * 帧时间
    if (方向 > 0 and c.位置.y >= c.归位目标Y) or
       (方向 < 0 and c.位置.y <= c.归位目标Y):
      c.位置.y = c.归位目标Y
      c.是否正在归位 = false

proc 触发落地归位*(c: var 摄像机2d; 目标Y: float32) {.inline.} =
  c.是否正在归位 = true
  c.归位目标Y = 目标Y

proc 更新边界推动*(c: var 摄像机2d) =
  ## 模式：玩家靠近屏幕边缘时推动摄像机
  c.屏幕偏移修正 = vec2(0, 0)
  let 屏幕宽 = getScreenWidth().float32
  let 屏幕高 = getScreenHeight().float32

  let 半宽推动 = 屏幕宽 * 0.5 * c.推动边界比.x / c.缩放
  let 半高推动 = 屏幕高 * 0.5 * c.推动边界比.y / c.缩放

  let 边界内左 = c.位置.x - 半宽推动
  let 边界内右 = c.位置.x + 半宽推动
  let 边界内上 = c.位置.y - 半高推动
  let 边界内下 = c.位置.y + 半高推动

  let 玩家 = c.跟随目标

  if 玩家.x < 边界内左: c.位置.x = 玩家.x
  if 玩家.y < 边界内上: c.位置.y = 玩家.y
  if 玩家.x > 边界内右: c.位置.x = 边界内左 + (玩家.x - 边界内右)
  if 玩家.y > 边界内下: c.位置.y = 边界内上 + (玩家.y - 边界内下)

# ============================================================================
# 每帧更新
# ============================================================================

proc 更新*(c: var 摄像机2d) =
  ## 每帧调用：按当前模式处理跟随 + 震动 + 边界

  let 帧时间 = getFrameTime()

  # ── 跟随 ──────────────────────────────────────
  if c.是否正在跟随:
    case c.模式
    of 居中跟随:   c.更新居中跟随()
    of 居中带边界: c.更新居中带边界()
    of 平滑跟随:   c.更新平滑跟随(帧时间)
    of 落地平滑:   c.更新落地平滑(帧时间)
    of 边界推动:   c.更新边界推动()

  # ── 世界边界约束（居中带边界有自带 offset 层，不走位置钳制） ──
  if c.是否启用边界 and c.模式 != 居中带边界:
    let 屏幕宽 = getScreenWidth().float32
    let 屏幕高 = getScreenHeight().float32
    let 半宽 = 屏幕宽 / (2 * c.缩放)
    let 半高 = 屏幕高 / (2 * c.缩放)
    c.位置.x = clamp(c.位置.x, c.世界左 + 半宽, c.世界右 - 半宽)
    c.位置.y = clamp(c.位置.y, c.世界上 + 半高, c.世界下 - 半高)

  # ── 屏幕震动 ─────────────────────────────────
  if c.震动强度 > 0.5:
    c.屏幕偏移 = vec2(
      rand(-c.震动强度 .. c.震动强度),
      rand(-c.震动强度 .. c.震动强度),
    )
    c.震动强度 *= c.震动衰减
  else:
    c.屏幕偏移 = vec2(0, 0)
    c.震动强度 = 0

# ============================================================================
# 便捷操作
# ============================================================================

proc 开始跟随*(c: var 摄像机2d; 目标: Vec2; 速度: float32 = -1) {.inline.} =
  c.跟随目标 = 目标
  c.是否正在跟随 = true
  if 速度 > 0: c.跟随速度 = 速度

proc 停止跟随*(c: var 摄像机2d) {.inline.} =
  c.是否正在跟随 = false

proc 瞬间移动到*(c: var 摄像机2d; 目标: Vec2) {.inline.} =
  c.位置 = 目标
  c.屏幕偏移修正 = vec2(0, 0)
  if c.是否正在跟随: c.跟随目标 = 目标

proc 缩放到*(c: var 摄像机2d; 倍率: float32) {.inline.} =
  c.缩放 = max(倍率, 0.001)

proc 缩放增量*(c: var 摄像机2d; 增量: float32) {.inline.} =
  c.缩放 = max(c.缩放 + 增量, 0.001)

# ============================================================================
# 验证
# ============================================================================

when isMainModule:
  block:
    var c = 新建摄像机2d(位置=vec2(100, 200), 缩放=2.0)
    assert c.位置 == vec2(100, 200)
    assert c.缩放 == 2.0

    for 模式 in [居中跟随, 居中带边界, 平滑跟随, 落地平滑, 边界推动]:
      c.模式 = 模式
      c.开始跟随(vec2(300, 400))
      c.更新()
      assert c.是否正在跟随
      c.停止跟随()

    c.缩放至光标(1.0)
    c.触发震动(10)
    c.更新()
    assert c.震动强度 < 10

    echo "✅ 2D 摄像机验证通过"
