## 2D 摄像机 — 平台游戏（官方示例移植）
##
## 官方 C 示例完整 Nim 移植，5 种摄像机模式，重力+跳跃+平台碰撞。
##
## ← → : 移动    Space : 跳跃
## C   : 切换模式     R : 重置
## 滚轮: 缩放
## ESC : 退出

import raylib
import ../mymath/vmath_raylib
import camera/摄像机2d
import vmath

# ============================================================================
# 常量
# ============================================================================
const
  重力         = 400
  跳跃速度     = 350.0
  水平速度     = 200.0
  窗口宽       = 800
  窗口高       = 450
  缩放下限     = 0.25
  缩放上限     = 3.0

# ============================================================================
# 类型
# ============================================================================
type
  玩家 = object
    位置: Vec2
    速度: float32
    是否可跳跃: bool

  平台 = object
    区域: Rectangle
    是否阻挡: bool
    颜色: Color

# ============================================================================
# 全局状态
# ============================================================================
var
  游戏玩家: 玩家
  平台列表: seq[平台]
  当前模式索引: int
  摄像机: 摄像机2d

const
  模式名列表 = [
    "中心跟随",
    "中心跟随 + 边界约束",
    "平滑中心跟随",
    "水平跟随 + 落地归位",
    "屏幕边缘推动",
  ]

# ============================================================================
# 初始化
# ============================================================================
proc 初始化游戏*() =
  游戏玩家 = 玩家(位置: vec2(400, 280), 速度: 0, 是否可跳跃: false)

  平台列表 = @[
    平台(区域: Rectangle(x: 0, y: 0,    width: 1000, height: 400), 是否阻挡: false, 颜色: LightGray),
    平台(区域: Rectangle(x: 0, y: 400,  width: 1000, height: 200), 是否阻挡: true,  颜色: Gray),
    平台(区域: Rectangle(x: 300, y: 200, width: 400,  height: 10), 是否阻挡: true,  颜色: Gray),
    平台(区域: Rectangle(x: 250, y: 300, width: 100,  height: 10), 是否阻挡: true,  颜色: Gray),
    平台(区域: Rectangle(x: 650, y: 300, width: 100,  height: 10), 是否阻挡: true,  颜色: Gray),
  ]

  摄像机 = 新建摄像机2d(
    位置 = 游戏玩家.位置,
    模式 = 居中跟随,
  )
  # 从平台列表计算世界边界
  var 左 = float32.high
  var 右 = float32.low
  var 上 = float32.high
  var 下 = float32.low
  for 平台 in 平台列表:
    左 = min(左, 平台.区域.x)
    右 = max(右, 平台.区域.x + 平台.区域.width)
    上 = min(上, 平台.区域.y)
    下 = max(下, 平台.区域.y + 平台.区域.height)
  摄像机.设置世界边界(左, 右, 上, 下)
  摄像机.开始跟随(游戏玩家.位置)

# ============================================================================
# 玩家更新（带重力与平台碰撞）
# ============================================================================
proc 更新玩家*(p: var 玩家; 帧时间: float32) =
  # 水平输入
  if isKeyDown(Left):  p.位置.x -= 水平速度 * 帧时间
  if isKeyDown(Right): p.位置.x += 水平速度 * 帧时间

  # 跳跃
  if isKeyPressed(Space) and p.是否可跳跃:
    p.速度 = -跳跃速度
    p.是否可跳跃 = false

  # 垂直物理 + 平台碰撞
  var 是否撞到 = false
  for 平台 in 平台列表:
    if 平台.是否阻挡 and
       平台.区域.x <= p.位置.x and
       平台.区域.x + 平台.区域.width >= p.位置.x and
       平台.区域.y >= p.位置.y and
       平台.区域.y <= p.位置.y + p.速度 * 帧时间:
      是否撞到 = true
      p.速度 = 0
      p.位置.y = 平台.区域.y
      break

  if 是否撞到:
    p.是否可跳跃 = true
  else:
    p.位置.y += p.速度 * 帧时间
    p.速度 += 重力 * 帧时间
    p.是否可跳跃 = false

# ============================================================================
# 绘制
# ============================================================================
proc 绘制场景*() =
  for 平台 in 平台列表:
    drawRectangle(平台.区域, 平台.颜色)

  # 玩家
  let 玩家区域 = Rectangle(
    x: 游戏玩家.位置.x - 20, y: 游戏玩家.位置.y - 40,
    width: 40, height: 40
  )
  drawRectangle(玩家区域, Red)
  drawCircle(游戏玩家.位置, 5, Gold)

proc 显示HUD*() =
  let 模式名 = 模式名列表[当前模式索引]

  drawText("操作说明:", 20, 20, 10, Black)
  drawText("- ← → 移动", 40, 40, 10, DarkGray)
  drawText("- Space 跳跃", 40, 60, 10, DarkGray)
  drawText("- 滚轮 缩放  |  R 重置", 40, 80, 10, DarkGray)
  drawText("- C 切换摄像机模式", 40, 100, 10, DarkGray)
  drawText("当前模式:", 20, 120, 10, Black)
  drawText(模式名, 40, 140, 10, DarkGray)

# ============================================================================
# 主入口
# ============================================================================
proc main =
  setConfigFlags(flags(WindowResizable))
  initWindow(窗口宽, 窗口高, "2D 摄像机 — 平台游戏（官方示例移植）")
  setTargetFPS(60)
  defer: closeWindow()

  初始化游戏()

  while not windowShouldClose():
    let 帧时间 = getFrameTime()

    # ── 更新 ────────────────────────────────────
    更新玩家(游戏玩家, 帧时间)

    # 滚轮缩放
    摄像机.缩放 += getMouseWheelMove() * 0.05
    摄像机.缩放 = clamp(摄像机.缩放, 缩放下限, 缩放上限)

    # R 重置
    if isKeyPressed(R):
      摄像机.缩放 = 1.0
      游戏玩家.位置 = vec2(400, 280)
      游戏玩家.速度 = 0
      游戏玩家.是否可跳跃 = false

    # C 切换模式
    if isKeyPressed(C):
      当前模式索引 = (当前模式索引 + 1) mod 模式名列表.len
      摄像机.模式 = 跟随模式(当前模式索引)

    # 落地归位检测
    if 游戏玩家.是否可跳跃 and 游戏玩家.速度 == 0 and
       游戏玩家.位置.y != 摄像机.位置.y:
      摄像机.触发落地归位(游戏玩家.位置.y)

    摄像机.跟随目标 = 游戏玩家.位置
    摄像机.更新()

    # ── 绘制 ────────────────────────────────────
    beginDrawing()
    clearBackground(LightGray)

    beginMode2D(摄像机.转Raylib())
    绘制场景()
    endMode2D()

    显示HUD()
    endDrawing()

when isMainModule:
  main()
