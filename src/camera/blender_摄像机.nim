## Blender 风格视口摄像机
##
## 模仿 Blender 3D 视口的摄像机操控逻辑：
##   - 中键拖拽：轨道旋转（绕枢轴点）
##   - Shift+中键拖拽：平移视图
##   - 滚轮：推拉缩放
##   - 小键盘 1/3/7：前/右/顶 视图
##   - Ctrl+小键盘 1/3/7：后/左/底 视图
##   - 小键盘 5：透视/正交 切换
##   - 小键盘 9：反向视图
##   - `.` (句号)：框选聚焦到目标位置
##
## 用法:
##   import camera/blender_摄像机
##   var 摄像机 = 新建视口摄像机(枢轴=vec3(0,0,0), 距离=10)
##   while not windowShouldClose():
##     摄像机.视口更新()   # 处理全部视口操作
##     beginMode3D(摄像机.转Raylib())
##       ...
##     endMode3D()

import vmath
import raylib
import mymath/vmath_raylib

export vmath

# ============================================================================
# 类型定义
# ============================================================================

type
  视口摄像机* = object
    ## 纯 vmath 视口摄像机，转换到 raylib 时才转为 Camera3D
    ##
    ## 内部使用「枢轴 + 方向 × 距离」模型，旋转绕枢轴，缩放调距离。
    枢轴*:    Vec3            ## 视口旋转/缩放中心点
    方向*:    Vec3            ## 枢轴→摄像机的单位方向向量
    距离*:    float32         ## 枢轴到摄像机的距离
    上向量*:  Vec3            ## 世界向上方向（默认 Y 轴）
    视野*:    float32         ## 垂直视野，角度制
    投影*:    CameraProjection

  # 标准视图方向
  标准视角* = enum
    视角正面    ## 前视图 -Z
    视角背面    ## 后视图 +Z
    视角右面    ## 右视图 +X
    视角左面    ## 左视图 -X
    视角顶面    ## 顶视图 +Y
    视角底面    ## 底视图 -Y

# ============================================================================
# 预定义标准视图方向
# ============================================================================
const
  标准视角方向: array[标准视角, Vec3] = [
    vec3(0, 0, -1),   # 正面
    vec3(0, 0,  1),   # 背面
    vec3(1, 0,  0),   # 右面
    vec3(-1, 0, 0),   # 左面
    vec3(0, 1,  0),   # 顶面
    vec3(0, -1, 0),   # 底面
  ]

# ============================================================================
# 构造函数
# ============================================================================

func 新建视口摄像机*(
  枢轴: Vec3 = vec3(0, 0, 0),
  距离: float32 = 10,
  方向: Vec3 = vec3(-1, 0.5, -1),
  上向量: Vec3 = vec3(0, 1, 0),
  视野: float32 = 45,
  投影: CameraProjection = Perspective
): 视口摄像机 {.inline.} =
  ## 创建一个 Blender 风格视口摄像机
  result = 视口摄像机(
    枢轴:   枢轴,
    方向:   方向.normalize(),
    距离:   距离,
    上向量: 上向量.normalize(),
    视野:   视野,
    投影:   投影,
  )

# ============================================================================
# 内部属性
# ============================================================================

func 位置*(c: 视口摄像机): Vec3 {.inline.} =
  ## 计算摄像机世界位置 = 枢轴 + 方向 × 距离
  c.枢轴 + c.方向 * c.距离

func 目标*(c: 视口摄像机): Vec3 {.inline.} =
  ## 摄像机注视目标点 = 枢轴
  c.枢轴

# ============================================================================
# 与 raylib Camera3D 互转
# ============================================================================

func 转Raylib*(c: 视口摄像机): Camera3D {.inline.} =
  ## 视口摄像机 → raylib Camera3D
  Camera3D(
    position: c.位置(),
    target:   c.目标(),
    up:       c.上向量,
    fovy:     c.视野,
    projection: c.投影
  )

# ============================================================================
# 球坐标辅助（偏航/俯仰）
# ============================================================================

type
  球坐标* = object
    偏航*: float32  ## 绕 Y 轴角度 (弧度)
    俯仰*: float32  ## 与水平面夹角 (弧度), 范围 (-PI/2, PI/2)

func 方向转球坐标*(方向: Vec3): 球坐标 {.inline.} =
  ## 方向向量 → 球坐标（偏航/俯仰）
  let 方向归一 = normalize(方向)
  球坐标(
    偏航: arctan2(方向归一.x, 方向归一.z),
    俯仰: arcsin(clamp(方向归一.y, -1, 1))
  )

func 球坐标转方向*(坐标: 球坐标): Vec3 {.inline.} =
  ## 球坐标 → 方向向量
  let cp = cos(坐标.俯仰)
  vec3(
    sin(坐标.偏航) * cp,
    sin(坐标.俯仰),
    cos(坐标.偏航) * cp
  )

# ============================================================================
# 轨道旋转（绕枢轴）
# ============================================================================

proc 轨道绕Y轴*(c: var 视口摄像机; 角度: float32) {.inline.} =
  ## 绕世界 Y 轴水平旋转（对应 Blender 中键左右拖拽）
  var 坐标 = 方向转球坐标(c.方向)
  坐标.偏航 += 角度
  c.方向 = 球坐标转方向(坐标)

const
  最大俯仰* = PI / 2 - 0.02  ## 俯仰限制：接近但不到极点

proc 轨道绕右轴*(c: var 视口摄像机; 角度: float32) {.inline.} =
  ## 绕摄像机右轴垂直旋转（对应 Blender 中键上下拖拽）
  var 坐标 = 方向转球坐标(c.方向)
  坐标.俯仰 = clamp(坐标.俯仰 + 角度, -最大俯仰, 最大俯仰)
  c.方向 = 球坐标转方向(坐标)

# ============================================================================
# 平移（移动枢轴和位置）
# ============================================================================

proc 水平平移*(c: var 视口摄像机; 偏移: Vec2) {.inline.} =
  ## 在垂直于视线的平面上平移（对应 Blender Shift+中键）
  var 参考上 = c.上向量
  # 方向接近垂直时避免 cross 退化
  if abs(dot(c.方向, c.上向量)) > 0.999:
    参考上 = vec3(0, 0, 1)

  let 右 = cross(c.方向, 参考上).normalize()
  let 上 = cross(右, c.方向).normalize()

  c.枢轴 = c.枢轴 + 右 * (-偏移.x) + 上 * 偏移.y

# ============================================================================
# 缩放（改变到枢轴的距离）
# ============================================================================

proc 缩放*(c: var 视口摄像机; 倍率: float32) {.inline.} =
  ## 推拉缩放：改变到枢轴的距离
  ## 倍率 > 1 拉远，< 1 推近
  c.距离 = clamp(c.距离 * 倍率, 0.01, 10000)

proc 滚轮缩放*(c: var 视口摄像机; 滚轮值: float32) {.inline.} =
  ## 滚轮缩放：每格增减 10%
  ## 滚轮值 > 0 推近，< 0 拉远
  c.缩放(1.0 - 滚轮值 * 0.1)

# ============================================================================
# 标准视角切换（带过渡）
# ============================================================================

proc 切换到视角*(c: var 视口摄像机; 视角: 标准视角) {.inline.} =
  ## 切换到标准视角（瞬间切换）
  c.方向 = 标准视角方向[视角]

proc 切换到反视角*(c: var 视口摄像机) {.inline.} =
  ## 切换到当前视角的反方向（Blender 小键盘 9）
  c.方向 = -c.方向

# ============================================================================
# 透视/正交切换
# ============================================================================

proc 切换投影*(c: var 视口摄像机) {.inline.} =
  ## 切换透视/正交（Blender 小键盘 5）
  c.投影 = if c.投影 == Perspective: Orthographic else: Perspective

# ============================================================================
# 聚焦到目标
# ============================================================================

proc 聚焦到*(c: var 视口摄像机; 目标点: Vec3) {.inline.} =
  ## 将视口中心移动到指定点（Blender 句号键）
  c.枢轴 = 目标点

proc 聚焦并调整距离*(c: var 视口摄像机; 目标点: Vec3; 包围盒半径: float32) {.inline.} =
  ## 聚焦到目标并自动调整距离以适配视野
  c.枢轴 = 目标点
  c.距离 = 包围盒半径 * 2.5 / tan(c.视野 * PI / 360)

# ============================================================================
# 光滑过渡（使用 vmath lerp/slerp）
# ============================================================================

type
  过渡动画* = object
    是否进行中*: bool
    起始方向*:  Vec3
    目标方向*:  Vec3
    起始距离*:  float32
    目标距离*:  float32
    起始枢轴*:  Vec3
    目标枢轴*:  Vec3
    经过时间*:  float32
    总时长*:    float32

proc 开始过渡*(c: 视口摄像机; 结果: var 过渡动画;
                目标方向: Vec3; 目标枢轴: Vec3;
                目标距离: float32; 时长: float32 = 0.3) =
  ## 启动光滑过渡
  结果 = 过渡动画(
    是否进行中: true,
    起始方向: c.方向,
    目标方向: 目标方向,
    起始距离: c.距离,
    目标距离: 目标距离,
    起始枢轴: c.枢轴,
    目标枢轴: 目标枢轴,
    总时长: 时长,
  )

proc 开始视角过渡*(c: 视口摄像机; 结果: var 过渡动画;
                    视角: 标准视角; 时长: float32 = 0.3) =
  ## 启动光滑过渡到标准视角
  c.开始过渡(结果, 标准视角方向[视角], c.枢轴, c.距离, 时长)

proc 更新过渡*(c: var 视口摄像机; 过渡: var 过渡动画; 帧时间: float32) =
  ## 更新过渡动画，返回是否完成
  if not 过渡.是否进行中:
    return

  过渡.经过时间 += 帧时间
  let t = min(过渡.经过时间 / 过渡.总时长, 1.0)
  # 缓入缓出
  let 插值 = t * t * (3 - 2 * t)

  c.方向 = normalize(mix(过渡.起始方向, 过渡.目标方向, 插值))
  c.距离 = mix(过渡.起始距离, 过渡.目标距离, 插值)
  c.枢轴 = mix(过渡.起始枢轴, 过渡.目标枢轴, 插值)

  if t >= 1.0:
    过渡.是否进行中 = false
    c.方向 = 过渡.目标方向.normalize()
    c.距离 = 过渡.目标距离
    c.枢轴 = 过渡.目标枢轴

# ============================================================================
# 完整视口更新（模仿 Blender 操作）
# ============================================================================

proc 视口更新*(c: var 视口摄像机; 过渡: var 过渡动画) =
  ## 处理全部 Blender 风格视口操作
  ## 每帧调用一次，替代手动调用各操作函数

  # 平滑过渡
  if 过渡.是否进行中:
    c.更新过渡(过渡, getFrameTime())

  # ── 标准视角切换 ──────────────────────────────
  if isKeyPressed(Kp1):
    if isKeyDown(LeftControl) or isKeyDown(RightControl):
      c.开始视角过渡(过渡, 视角背面)
    else:
      c.开始视角过渡(过渡, 视角正面)

  if isKeyPressed(Kp3):
    if isKeyDown(LeftControl) or isKeyDown(RightControl):
      c.开始视角过渡(过渡, 视角左面)
    else:
      c.开始视角过渡(过渡, 视角右面)

  if isKeyPressed(Kp7):
    if isKeyDown(LeftControl) or isKeyDown(RightControl):
      c.开始视角过渡(过渡, 视角底面)
    else:
      c.开始视角过渡(过渡, 视角顶面)

  # 反向视角
  if isKeyPressed(Kp9):
    c.开始过渡(过渡, -c.方向, c.枢轴, c.距离)

  # 透视/正交切换
  if isKeyPressed(Kp5):
    c.切换投影()

  # ── 鼠标中键：轨道旋转 ────────────────────────
  if isMouseButtonDown(Middle) and not isKeyDown(LeftShift) and not isKeyDown(RightShift):
    let 鼠标偏移 = getMouseDelta()
    c.轨道绕Y轴(-鼠标偏移.x * 0.005)
    c.轨道绕右轴(-鼠标偏移.y * 0.005)
    # 轨道时取消过渡（用户手动操作打断动画）
    过渡.是否进行中 = false

  # ── Shift+中键：平移 ──────────────────────────
  if isMouseButtonDown(Middle) and (isKeyDown(LeftShift) or isKeyDown(RightShift)):
    let 鼠标偏移 = getMouseDelta()
    let 灵敏度 = c.距离 * 0.002
    c.水平平移(vec2(鼠标偏移.x * 灵敏度, 鼠标偏移.y * 灵敏度))
    过渡.是否进行中 = false

  # ── 滚轮：缩放 ────────────────────────────────
  let 滚轮 = getMouseWheelMove()
  if 滚轮 != 0:
    c.滚轮缩放(滚轮)
    过渡.是否进行中 = false

  # ── 句号键：聚焦到枢轴 ────────────────────────
  if isKeyPressed(Period):
    # 重置枢轴为当前目标点
    # 使用 getScreenToWorldRay 计算鼠标下的世界点会比较复杂
    # 这里简化为聚焦到当前枢轴
    discard

# ============================================================================
# 便捷：获取视口摄像机对应的 raylib Camera3D（用于 beginMode3D）
# ============================================================================

converter 转Camera3D*(c: 视口摄像机): Camera3D {.inline.} =
  ## 隐式转换，可直接传入 beginMode3D
  c.转Raylib()

