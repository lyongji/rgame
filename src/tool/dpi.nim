## 跨平台窗口 DPI 自适应工具模块
##
## 自动检测显示器 DPI 缩放比，适配各平台（macOS Retina / Windows / Linux Wayland & X11），
## 按基准分辨率等比缩放窗口大小，返回缩放比供字体、坐标等 UI 元素使用。
##
## 快速开始
## ==========
##
##   import tool/dpi
##
##   let cfg = 初始化窗口("我的应用", 基准宽 = 1280, 基准高 = 720)
##   defer: closeWindow()
##
##   # 缩放比应用于字号/坐标
##   let 字号 = int32(18 * cfg.缩放比)
##   drawText("你好", 20, 20, 字号, White)

import raylib

# ═══════════════════════════════════════════════════
# 类型定义
# ═══════════════════════════════════════════════════

type
  窗口配置* = object
    ## 窗口自适应结果
    缩放比*: float32      # 相对于基准分辨率的缩放比（如 1.0、1.5、2.0、2.31）
    屏幕宽*: int32        # 最终窗口宽度（像素）
    屏幕高*: int32        # 最终窗口高度（像素）
    显示器宽*: int32      # 显示器原始宽度
    显示器高*: int32      # 显示器原始高度
    dpi原始*: float32     # 系统报告的 DPI 缩放（不依赖回退逻辑的原始值）

# ═══════════════════════════════════════════════════
# 内部常量
# ═══════════════════════════════════════════════════

const
  # 显示器宽超过此值认为是高 DPI 显示器（4K 起步），触发 X11 回退逻辑
  高DPI阈值* {.intdefine.} = 2560

  # 窗口占显示器宽度的比例
  默认占比* = 0.65

  # 窗口高度上限占显示器高度的比例
  高度上限* = 0.9

# ═══════════════════════════════════════════════════
# DPI 检测
# ═══════════════════════════════════════════════════

proc 检测系统DPI(): float32 =
  ## 获取系统报告的 DPI 缩放比（内部使用）。
  let dpi = getWindowScaleDPI()
  dpi.x

# ═══════════════════════════════════════════════════
# 窗口初始化（核心 API）
# ═══════════════════════════════════════════════════

proc 初始化窗口*(标题: string,
                  基准宽: int32 = 1280,
                  基准高: int32 = 720,
                  占比: float32 = 默认占比,
                  初始宽: int32 = 800,
                  初始高: int32 = 600): 窗口配置 =
  ## 创建窗口并自动适配显示器 DPI。
  ##
  ## 只此一个函数，内部自动完成：
  ##   1. 以 `初始宽`×`初始高` 创建窗口（瞬间闪过）
  ##   2. 读取显示器分辨率 + 系统 DPI
  ##   3. 各平台自适应缩放（macOS Retina / Windows / Wayland / X11）
  ##   4. 等比缩放窗口至合适大小
  ##   5. 返回 `窗口配置`（含缩放比，供字号/坐标缩放用）
  ##
  ## 用法:
  ##   import tool/dpi
  ##   let cfg = 初始化窗口("我的应用")
  ##   defer: closeWindow()
  ##   let 字号 = int32(18 * cfg.缩放比)
  ##   drawText("你好", 20, 20, 字号, White)
  ##
  ## 参数:
  ##   `标题`   — 窗口标题
  ##   `基准宽` — 理想的逻辑宽度（缩放比 = 屏幕宽 / 基准宽）
  ##   `基准高` — 理想的逻辑高度（用于窗口按比例缩放）
  ##   `占比`   — 窗口占显示器宽度的比例（X11 回退时使用）
  ##   `初始宽` — 创建窗口的临时宽度（建议保持默认）
  ##   `初始高` — 创建窗口的临时高度
  initWindow(初始宽, 初始高, 标题)

  let 显示器宽 = getMonitorWidth(getCurrentMonitor())
  let 显示器高 = getMonitorHeight(getCurrentMonitor())

  # ── 获取系统 DPI ──
  let 系统DPI = 检测系统DPI()

  # ── 确定缩放比 ──
  # X11 下系统 DPI 总是 1.0，若显示器分辨率超过阈值则用尺寸计算
  let 缩放比 = if 系统DPI <= 1.01 and 显示器宽 > 高DPI阈值:
                 显示器宽.float32 * 占比 / 基准宽.float32
               else:
                 系统DPI

  # ── 计算窗口尺寸 ──
  let 屏幕宽 = int32(基准宽.float32 * 缩放比)
  let 屏幕高 = min(int32(基准高.float32 * 缩放比),
                   int32(显示器高.float32 * 高度上限))

  setWindowSize(屏幕宽, 屏幕高)

  窗口配置(
    缩放比:   缩放比,
    屏幕宽:   屏幕宽,
    屏幕高:   屏幕高,
    显示器宽: 显示器宽,
    显示器高: 显示器高,
    dpi原始:  系统DPI,
  )

# ═══════════════════════════════════════════════════
# 验证测试
# ═══════════════════════════════════════════════════

when isMainModule:
  echo "窗口 DPI 自适应模块 — 验证测试"
  echo "════════════════════════════════"
  echo "高DPI阈值: ", 高DPI阈值
  echo "默认占比: ", 默认占比
  echo "高度上限: ", 高度上限
  echo ""

  # 模拟测试：检测逻辑（内联）
  block 回退逻辑测试:
    proc x11回退(dpi: float32, w: int32): bool = dpi <= 1.01 and w > 高DPI阈值
    assert not x11回退(2.0, 3840)  # macOS Retina
    assert x11回退(1.0, 3840)      # 4K X11
    assert not x11回退(1.0, 1920)  # FHD X11
    assert not x11回退(2.0, 1920)  # Wayland 2x
    echo "✅ 回退逻辑测试通过"

  echo ""
  echo "所有验证通过"
